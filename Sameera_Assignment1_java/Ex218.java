public class Ex218 {
    
        public static void main(String[] args) {
            // Box
            System.out.println("******");
            System.out.println("*    *");
            System.out.println("*    *");
            System.out.println("******\n");
    
    
            // Oval
            System.out.println("  ***  ");
            System.out.println(" *   * ");
            System.out.println("*     *");
            System.out.println(" *   * ");
            System.out.println("  ***  \n");
    
            // Arrow
            System.out.println("  *  ");
            System.out.println(" *** ");
            System.out.println("*****");
            System.out.println("  *  ");
            System.out.println("  *  \n");
    
            // Diamond
            System.out.println("   *   ");
            System.out.println("  * *  ");
            System.out.println(" *   * ");
            System.out.println("  * *  ");
            System.out.println("   *   ");
        }
    }
    

